CREATE TABLE dept (
	ND 		INTEGER PRIMARY KEY,
	NOM 		TEXT,
	VILLE 		TEXT
);

CREATE TABLE emp (
	NE 		INTEGER PRIMARY KEY,
	NOM 		TEXT,
	JOB 		TEXT,
	DATEMB  	TEXT,
	SAL 		REAL,
	COMM 		REAL,
	ND 		INTEGER, 
	NEchef          INTEGER,
	FOREIGN KEY(ND) REFERENCES dept(ND),
	FOREIGN KEY(NEchef) REFERENCES emp(NE)
);
